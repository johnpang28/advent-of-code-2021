import Day10.complete
import Day10.input
import Day10.findWrongChar
import Day10.scoreMap1
import Day10.scoreMap2
import java.util.*

fun main() {

    val answer1 = input.mapNotNull { findWrongChar(it)?.let { c -> scoreMap1.getValue(c) } }.sum()
    println(answer1) // 311949

    val completions = input.filter { findWrongChar(it) == null }.map { complete(it) }
    val scores = completions.map { c -> c.map { scoreMap2.getValue(it) }.fold(0L) { acc, n -> acc * 5 + n } }
    val answer2 = scores.sorted()[scores.size / 2]
    println(answer2) // 3042730309
}

