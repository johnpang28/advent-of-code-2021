        line.forEach { c ->
            if (bracketsMap.keys.contains(c)) stack.push(c)
            else if (reversedBracketsMap[c] != stack.pop()) return c
        }
        return null
    }

    fun complete(line: String): String {
        val stack = Stack<Char>()
        line.forEach { c ->
            if (bracketsMap.keys.contains(c)) stack.push(c)
            else stack.pop()
        }
        var completion = ""
        while (stack.isNotEmpty()) {
            completion += bracketsMap[stack.pop()]
        }
        return completion
    }

